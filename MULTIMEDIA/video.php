            <h2>Visual Effects & Compositing</h2>
            <p><span style="color: #28A745; font-weight: bold;">Visual effects (VFX)</span> are essential tools that can elevate your video project by adding elements that would be impossible or impractical to capture in-camera. From <span style="color: #28A745; font-weight: bold;">green screen compositing</span> to <span style="color: #28A745; font-weight: bold;">3D animation</span>, VFX can transform your vision into reality.</p>
            
            <h3>Motion Graphics</h3>
            <p><span style="color: #28A745; font-weight: bold;">Motion graphics</span> combine graphic design and animation to create visually engaging content. Programs like Adobe After Effects allow you to <span style="color: #28A745; font-weight: bold;">animate text, create dynamic transitions</span>, and incorporate elements like lower thirds or logo animations into your videos.</p>
            
            <h3>3D Animation</h3>
            <p><span style="color: #28A745; font-weight: bold;">3D animation</span> adds depth and realism to your projects, whether you're creating a full animated scene or adding subtle effects like <span style="color: #28A745; font-weight: bold;">realistic shadows and reflections</span>. Tools like Blender or Cinema 4D offer powerful capabilities for creating 3D content.</p>
            
            <h3>Compositing</h3>
            <p><span style="color: #28A745; font-weight: bold;">Compositing</span> involves combining multiple visual elements into a single image or scene. Techniques like <span style="color: #28A745; font-weight: bold;">green screen keying, masking, and tracking</span> allow you to seamlessly integrate actors into virtual environments or create <span style="color: #28A745; font-weight: bold;">complex visual effects</span> that enhance the narrative.</p>
        </div>
    </section>

    <section id="color-grading" class="section">
        <div class="content-wrapper">
            <h2>Color Grading & Correction</h2>
            <p><span style="color: #28A745; font-weight: bold;">Color grading</span> is the process of enhancing or altering the color of your video to achieve a specific mood or style. Whether you're aiming for a <span style="color: #28A745; font-weight: bold;">cinematic look</span> or a <span style="color: #28A745; font-weight: bold;">naturalistic feel</span>, color grading is a crucial step in the post-production process.</p>
            
            <h3>Color Correction</h3>
            <p><span style="color: #28A745; font-weight: bold;">Color correction</span> involves adjusting the footage to ensure consistent color balance and exposure across all shots. This process corrects any issues from the original shoot, like underexposure or white balance errors, ensuring that the video <span style="color: #28A745; font-weight: bold;">looks professional</span>.</p>
            
            <h3>Creative Grading</h3>
            <p><span style="color: #28A745; font-weight: bold;">Creative grading</span> allows you to stylize your video with a unique look. Whether it's emulating the <span style="color: #28A745; font-weight: bold;">teal-orange look of blockbuster films</span> or creating a vintage feel, color grading tools in software like DaVinci Resolve enable you to <span style="color: #28A745; font-weight: bold;">craft the visual tone</span> that best suits your project.</p>
            
            <h3>LUTs (Look-Up Tables)</h3>
            <p><span style="color: #28A745; font-weight: bold;">LUTs</span> are presets that apply a specific color grade to your footage. They can save time and ensure consistency across a project. You can create custom LUTs or use pre-made ones to achieve a particular look, such as a <span style="color: #28A745; font-weight: bold;">filmic grade</span> or a <span style="color: #28A745; font-weight: bold;">stylized color palette</span>.</p>
